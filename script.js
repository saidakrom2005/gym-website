// Router function with language support
function router() {
    const hash = window.location.hash.substring(2) || '/';
    const page = hash.split('/')[0] || 'home';
    const appContent = document.getElementById('app-content');
    
    // Get current language from localStorage (default: ru)
    const currentLang = localStorage.getItem('selectedLanguage') || 'ru';
    
    // Build page key with language suffix
    const langSuffix = currentLang === 'ru' ? 'RU' : (currentLang === 'en' ? 'ENG' : 'UZ');
    const pageKey = page + langSuffix;
    
    // Get the page content (fallback to RU if translation doesn't exist)
    const content = pages[pageKey] || pages[page + 'RU'] || pages['homeRU'];
    appContent.innerHTML = content;
    
    // Update active state in navigation
    // Remove 'current' class from all nav items
    document.querySelectorAll('[data-page]').forEach(link => {
        link.parentElement.classList.remove('current');
    });
    
    // Add 'current' class to the active navigation link
    const activeLink = document.querySelector(`[data-page="${page}"]`);
    if (activeLink) {
        activeLink.parentElement.classList.add('current');
    }
}

// Initialize router on page load
document.addEventListener('DOMContentLoaded', function() {
    router();
});

// Listen for hash changes and update page
window.addEventListener('hashchange', function() {
    router();
});

// Also add click handlers to navigation links to ensure routing works
document.addEventListener('click', function(e) {
    const link = e.target.closest('a[data-page]');
    if (link) {
        const page = link.getAttribute('data-page');
        window.location.hash = '#/' + page;
    }
});

// Burger (collapse) toggle handler: toggles the menu target specified in data-target
function setupBurgerToggle() {
    document.addEventListener('click', function (e) {
        const toggle = e.target.closest('[data-toggle="collapse"]');
        if (!toggle) return;
        
        const targetSelector = toggle.getAttribute('data-target');
        if (!targetSelector) return;
        
        const target = document.querySelector(targetSelector);
        if (!target) return;
        
        // Prevent default button behavior
        e.preventDefault();
        e.stopPropagation();
        
        // Toggle 'in' class (Bootstrap convention: .collapse.in means expanded)
        target.classList.toggle('in');
        console.log('Burger toggled. Menu open:', target.classList.contains('in'));
    });
}

// Initialize burger toggle when DOM is ready
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', setupBurgerToggle);
} else {
    setupBurgerToggle();
}

// Close burger menu when routing to a new page
// (ensures mobile menu hides after navigation)
const closeBurgerOnRoute = () => {
    const nav = document.querySelector('.nav-collapse');
    if (nav && nav.classList.contains('in')) nav.classList.remove('in');
};

// Wrap original router to also close burger menu
const _originalRouter = router;
router = function () {
    _originalRouter();
    closeBurgerOnRoute();
};

// Language dropdown functionality
const setupLanguageDropdown = () => {
    const langOptions = document.querySelectorAll('.lang-option');
    const langToggle = document.querySelector('.lang-toggle');
    let currentLang = localStorage.getItem('selectedLanguage') || 'ru';

    // Set initial active language
    updateLanguageDisplay(currentLang);

    // Handle language option clicks
    langOptions.forEach(option => {
        option.addEventListener('click', (e) => {
            e.preventDefault();
            const lang = option.dataset.lang;
            currentLang = lang;
            localStorage.setItem('selectedLanguage', lang);
            updateLanguageDisplay(lang);
            
            // Close dropdown after selection - target the new class
            const langMenu = document.querySelector('.lang-menu-dropdown');
            if (langMenu && langMenu.classList.contains('in')) {
                langMenu.classList.remove('in');
            }
            
            // Reload current page in new language
            router();
        });
    });

    // Update language display
    function updateLanguageDisplay(lang) {
        langOptions.forEach(option => option.classList.remove('active'));
        document.querySelector(`.lang-option[data-lang="${lang}"]`).classList.add('active');
        
        const langNames = { ru: 'RU', en: 'EN', uz: 'O\'Z' };
        langToggle.textContent = langNames[lang] + ' ▼';
        
        console.log('Language changed to:', lang);
    }
};

// Initialize language dropdown when DOM is ready
if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', setupLanguageDropdown);
} else {
    setupLanguageDropdown();
}