// Router function
function router() {
    const hash = window.location.hash.substring(2) || '/';
    const page = hash.split('/')[0] || 'home';
    const appContent = document.getElementById('app-content');
    
    // Get the page content
    const content = pages[page] || pages['home'];
    appContent.innerHTML = content;
    
    // Update active state in navigation
    // Remove 'current' class from all nav items
    document.querySelectorAll('[data-page]').forEach(link => {
        link.parentElement.classList.remove('current');
    });
    
    // Add 'current' class to the active navigation link
    const activeLink = document.querySelector(`[data-page="${page}"]`);
    if (activeLink) {
        activeLink.parentElement.classList.add('current');
    }
}

// Initialize router on page load
document.addEventListener('DOMContentLoaded', function() {
    router();
});

// Listen for hash changes and update page
window.addEventListener('hashchange', function() {
    router();
});

// Also add click handlers to navigation links to ensure routing works
document.addEventListener('click', function(e) {
    const link = e.target.closest('a[data-page]');
    if (link) {
        const page = link.getAttribute('data-page');
        window.location.hash = '#/' + page;
    }
});

// Burger (collapse) toggle handler: toggles the menu target specified in data-target
document.addEventListener('click', function (e) {
    const toggle = e.target.closest('[data-toggle="collapse"]');
    if (!toggle) return;
    e.preventDefault();
    const targetSelector = toggle.getAttribute('data-target');
    if (!targetSelector) return;
    const target = document.querySelector(targetSelector);
    if (!target) return;
    // Toggle a simple 'open' class (CSS can style .nav-collapse.open to show)
    target.classList.toggle('open');
});

// Close burger menu when routing to a new page
// (ensures mobile menu hides after navigation)
const closeBurgerOnRoute = () => {
    const nav = document.querySelector('.nav-collapse');
    if (nav && nav.classList.contains('open')) nav.classList.remove('open');
};

// Wrap original router to also close burger menu
const _originalRouter = router;
router = function () {
    _originalRouter();
    closeBurgerOnRoute();
};